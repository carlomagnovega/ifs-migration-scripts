DECLARE   
   CURSOR get_qaman IS
      SELECT   a.invoice_id,
               a.CF$_AIM_ACCRUAL_AMT,
               (SELECT sum(nvl(CF$_AIM_INVOICED_COST,0)) FROM INVOICE_ITEM_CFV b WHERE b.INVOICE_ID = a.INVOICE_ID) INVOICED_COST,
               (SELECT sum(nvl(CF$_AIM_FREIGHT_COST,0)) FROM INVOICE_ITEM_CFV b WHERE b.INVOICE_ID = a.INVOICE_ID) FREIGHT_COST
               
      FROM INVOICE_CFV a       
      WHERE a.OBJSTATE <> 'Posted' 	  
      AND a.CF$_AIM_MATCHED_INV IS NOT NULL
      AND nvl(a.CF$_AIM_ACCRUAL_AMT,0) > 0
      AND (SELECT sum(nvl(CF$_AIM_INVOICED_COST,0)) FROM INVOICE_ITEM_CFV b WHERE b.INVOICE_ID = a.INVOICE_ID) = 0
      AND (SELECT sum(nvl(CF$_AIM_FREIGHT_COST,0)) FROM INVOICE_ITEM_CFV b WHERE b.INVOICE_ID = a.INVOICE_ID) <> 0;
   
   info_                VARCHAR2(32000);
   objid_               VARCHAR2(32000);
   objversion_          VARCHAR2(32000);
   attr_cf_             VARCHAR2(26000);
   attr_                VARCHAR2(26000); 
BEGIN
   Client_SYS.Clear_Attr(attr_);   
   FOR row_ IN get_qaman LOOP
      
      INVOIC_AIM_API.Update_Matched_Freight(row_.invoice_id);
      COMMIT;
   END LOOP;   
END;