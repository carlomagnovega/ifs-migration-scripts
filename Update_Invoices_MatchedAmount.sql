DECLARE   
   CURSOR get_qaman IS
      SELECT   a.OBJID,
               a.invoice_id,
               b.INVOICE_NO,
               b.amount
      FROM INVOICE_CFV a 
      INNER JOIN MTK_MATCHED_INVOICE_TAB b ON b.COMPANY =  a.COMPANY AND b.INVOICE_NO = a.INVOICE_NO
      WHERE a.OBJSTATE <> 'Posted' 	  
      AND a.CF$_AIM_MATCHED_INV IS NULL;
   
   info_                VARCHAR2(32000);
   objid_               VARCHAR2(32000);
   objversion_          VARCHAR2(32000);
   attr_cf_             VARCHAR2(26000);
   attr_                VARCHAR2(26000); 
BEGIN
   Client_SYS.Clear_Attr(attr_);   
   FOR row_ IN get_qaman LOOP
      objid_ := row_.objid;
      Client_SYS.Clear_Attr(attr_cf_);
      Client_SYS.Add_To_Attr('CF$_AIM_MATCHED_INV', 'Forced Matched', attr_cf_);     
      Client_SYS.Add_To_Attr('CF$_AIM_ACCRUAL_AMT', row_.amount, attr_cf_);     
 
      INVOICE_CFP.Cf_Modify__(info_, objid_, attr_cf_, attr_, 'DO');
      INVOIC_AIM_API.Update_Matched_Freight(row_.invoice_id);
      COMMIT;
   END LOOP;   
END;


